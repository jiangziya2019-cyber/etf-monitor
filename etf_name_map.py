def get_etf_name(code: str, default: str = '未知') -> str:
    """
    获取 ETF 名称
    
    Args:
        code: ETF 代码（6 位数字）
        default: 找不到时的默认值
    
    Returns:
        ETF 名称，找不到返回 default
    """
    return ETF_NAME_MAP.get(code, default)


def get_etf_names(codes: list) -> dict:
    """
    批量获取 ETF 名称
    
    Args:
        codes: ETF 代码列表
    
    Returns:
        {code: name, ...} 字典
    """
    return {code: get_etf_name(code) for code in codes}


def format_etf(code: str, with_name: bool = True) -> str:
    """
    格式化 ETF 显示（代码 + 名称）
    
    Args:
        code: ETF 代码
        with_name: 是否包含名称
    
    Returns:
        格式化字符串，如 "510300 沪深 300ETF" 或 "510300"
    """
    if not with_name:
        return code
    name = get_etf_name(code)
    return f"{code} {name}"


